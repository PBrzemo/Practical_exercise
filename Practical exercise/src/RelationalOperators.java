public class RelationalOperators {
    public static void main(String[] args){
        /*
            comparison
            a == b
            != compare different from each other
            > bigger than
            < smaller than
            >= greater or equal
            =< smaller or equal

         */
        int a = 1, b =2;
        System.out.println( a==b );
        boolean test = 2==2;
        System.out.println(test);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);

    }
}

