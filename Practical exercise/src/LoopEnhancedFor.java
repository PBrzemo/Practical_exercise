public class LoopEnhancedFor {
    static public void main(String[] args){
        /*
             for(type container name: name of the element)
             {
                command(container name);
             }
         */
        int [] tab = {1,2,3,4,5,6,7};
        for(int tabName: tab){
            System.out.println(tabName);
        }
    }
}
